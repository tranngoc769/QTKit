import os
import sys
import subprocess
import shutil

def print_header(title):
    """Print formatted header"""
    print("\n" + "=" * 50)
    print(f"üöÄ {title}")
    print("=" * 50)

def clean_build():
    """Clean previous builds"""
    print("üßπ Cleaning previous builds...")
    dirs_to_clean = ["build", "dist", "__pycache__"]
    for dir_name in dirs_to_clean:
        if os.path.exists(dir_name):
            shutil.rmtree(dir_name)
            print(f"  Removed: {dir_name}/")

def check_requirements():
    """Check and install requirements"""
    print("üì¶ Checking requirements...")
    
    required_files = ["main.py", "logo.png", "requirements.txt"]
    for file in required_files:
        if not os.path.exists(file):
            print(f"‚ùå Required file not found: {file}")
            return False
        print(f"‚úÖ Found: {file}")
    
    # Install Python requirements
    try:
        subprocess.run([sys.executable, "-m", "pip", "install", "-r", "requirements.txt"], 
                      check=True, capture_output=True)
        print("‚úÖ Python requirements installed")
    except subprocess.CalledProcessError as e:
        print(f"‚ùå Failed to install requirements: {e}")
        return False
    
    # Check/install PyInstaller
    try:
        subprocess.run(["pyinstaller", "--version"], capture_output=True, check=True)
        print("‚úÖ PyInstaller found")
    except:
        print("üì¶ Installing PyInstaller...")
        subprocess.run([sys.executable, "-m", "pip", "install", "pyinstaller"], check=True)
        print("‚úÖ PyInstaller installed")
    
    return True

def build_app():
    """Build the app using PyInstaller"""
    print("üî® Building QTKit app...")
    
    cmd = [
        "pyinstaller",
        "--name=QTKit",
        "--onedir",
        "--windowed", 
        "--icon=logo.png",
        "--osx-bundle-identifier=com.qt-corporation.qtkit",
        "--add-data", "logo.png:.",
        "--hidden-import=PySide6.QtCore",
        "--hidden-import=PySide6.QtGui",
        "--hidden-import=PySide6.QtWidgets", 
        "--hidden-import=pynput.keyboard",
        "--hidden-import=AppKit",
        "--clean",
        "--noconfirm",
        "main.py"
    ]
    
    try:
        result = subprocess.run(cmd, check=True, capture_output=True, text=True)
        print("‚úÖ App built successfully!")
        return True
    except subprocess.CalledProcessError as e:
        print(f"‚ùå Build failed: {e}")
        if e.stderr:
            print(f"Error details: {e.stderr}")
        return False

