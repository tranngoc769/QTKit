def fix_distribution():
    """Fix distribution issues (quarantine, signing)"""
    print("üîß Fixing distribution issues...")
    
    app_path = "dist/QTKit.app"
    if not os.path.exists(app_path):
        print("‚ùå App not found")
        return False
    
    # Step 1: Remove quarantine attributes
    print("  üîì Removing quarantine attributes...")
    try:
        cmd = ["xattr", "-dr", "com.apple.quarantine", app_path]
        subprocess.run(cmd, capture_output=True)
        
        # Remove other problematic attributes
        attrs_to_remove = [
            "com.apple.FinderInfo",
            "com.apple.metadata:kMDItemWhereFroms"
        ]
        
        for attr in attrs_to_remove:
            try:
                cmd = ["xattr", "-dr", attr, app_path]
                subprocess.run(cmd, capture_output=True)
            except:
                pass
        
        print("  ‚úÖ Quarantine attributes removed")
    except Exception as e:
        print(f"  ‚ö†Ô∏è Could not remove quarantine: {e}")
    
    # Step 2: Apply ad-hoc signature
    print("  ‚úçÔ∏è Applying ad-hoc signature...")
    try:
        cmd = [
            "codesign",
            "--force",
            "--deep",
            "--sign", "-",  # Ad-hoc signature
            app_path
        ]
        
        subprocess.run(cmd, check=True, capture_output=True)
        print("  ‚úÖ Ad-hoc signature applied")
        
        # Verify signature
        verify_cmd = ["codesign", "--verify", "--verbose", app_path]
        result = subprocess.run(verify_cmd, capture_output=True)
        if result.returncode == 0:
            print("  ‚úÖ Signature verified")
        
    except subprocess.CalledProcessError as e:
        print(f"  ‚ö†Ô∏è Signing failed: {e}")
    
    return True

